
#tuple
mixed_tuple = (2.2, "hello", 10, True, 10)
print(type(mixed_tuple))  # <class 'tuple'>
print("printing tuple = " + str(mixed_tuple))
print(mixed_tuple.count(10))  # Count occurrences of 10

#array
numbers = [1, 2, 3, 4, 5]
print(type(numbers))  # <class 'list'>
print(len(numbers))  # Length of the list
numbers.append(6)  # Adding an element to the list
print(len(numbers))


